
		<h1 style='text-align:center'>HISTOIRE</h1>

<p>Après avoir voyagé en Australie et en Nouvelle-Zélande, j’ai passé la fin de l’année 2015 en Martinique pour y effectuer un stage. Là, mes conditions de logement étaient sommaires : Petit appartement sans climatisation, vue sur le parking et pour colocataires : Les moustiques…</p>
<p>C’est alors que je me suis mis à rêver d’un jardin frais et ombragé avec piscine et transats !</p>
<p>La date de mon anniversaire approchait, je cherchais un moyen d’inviter mes amis à une soirée festive avec Barbecue et musique.</p>
<p>J’ai recherché sur internet s’il existait des sites de locations de jardins entre particuliers, cela n’existait pas.</p>
<p>C’est ainsi que mon concept est né et s’est affiné en partant du besoin pour un citadin de renouer avec la nature.</p>
<p>L’urbanisation et la société de consommation nous ont éloigné de cet élément, nous ressentons aujourd’hui la nécessité d’un retour à des valeurs saines, universelles communes à tous, favorisant ainsi le partage et le bien être.</p>
<p>Faire revivre ce lien privilégié entre la terre et le cosmos est essentiel pour l’homme et se concrétise dans l’élaboration du jardin.</p>
<p>Selon une étude effectuée par les chercheurs de l’Université de Stanford, le contact de l’homme avec la nature favorise le bien-être, régule notre rythme biologique et renforce notre champ énergétique individuel.</p>
<p>Le jardin éveille les sens, transcende nos émotions et rend nos souvenirs intemporels. Ce lieu se prête à tous les évènements qui constituent les étapes de l’existence.</p>


<h1 style='text-align:center'>KERDEN</h1>

<p>Mon nom de famille KERDELHUE d’origine bretonne débute par KER qui signifie : « Un lieu habité ».</p>
<p>Ce préfixe est utilisé dans de très nombreux noms bretons et identifie l’origine bretonne de ces noms.</p>
<p>Le jardin, m’évoquait quelque chose de symbolique et d’universel : Le jardin d’Eden.</p>
<p>C’est en associant mes racines à un concept universel qu’est née ma société KERDEN.</p>
<p>Kerden ouvre un nouveau paradigme qui relie la technologie, à la nature en y associant tous les membres de la société.</p>



